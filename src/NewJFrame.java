    private void BtnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRetirarActionPerformed
BtnRetirar.addActionListener(new ActionListener() {
    public void actionPerformed(ActionEvent e) {
        DefaultTableModel modelo1 = (DefaultTableModel) TabelaAdd.getModel();
        DefaultTableModel modelo2 = (DefaultTableModel) TabelaRet.getModel();
        
        // Verifica se a tabela1 tem pelo menos uma linha
        if (modelo1.getRowCount() > 0) {
            // Obtém o valor da primeira célula da primeira linha da tabela1
            Object valor = modelo1.getValueAt(0, 0);
            
            // Remove a primeira linha da tabela1
            modelo1.removeRow(0);
            
            // Adiciona o valor à tabela2
            modelo2.addRow(new Object[] { valor });
        } else{
            JOptionPane.showMessageDialog(null, "Por favor adicione um dado a tabela!");
        }
    }
});

    }//GEN-LAST:event_BtnRetirarActionPerformed

