    private void CbxSexoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CbxSexoKeyPressed
        JComboBox<String> comboBox = new JComboBox<String>();
comboBox.addItem("Item 1");
comboBox.addItem("Item 2");
comboBox.addItem("Item 3");

// Adiciona um ouvinte de teclado à caixa de combinação
comboBox.addKeyListener(new KeyAdapter() {
    @Override
    public void keyPressed(KeyEvent e) {
        // Obtém a tecla pressionada
        int keyCode = e.getKeyCode();
        
        // Seleciona o item apropriado com base na tecla pressionada
        if (keyCode == KeyEvent.VK_1) {
            comboBox.setSelectedIndex(0);
        } else if (keyCode == KeyEvent.VK_2) {
            comboBox.setSelectedIndex(1);
        } else if (keyCode == KeyEvent.VK_3) {
            comboBox.setSelectedIndex(2);
        }
    }
});
if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            BtnAdicionar.requestFocus();
        }

    }//GEN-LAST:event_CbxSexoKeyPressed

